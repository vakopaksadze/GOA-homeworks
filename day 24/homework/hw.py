import random

secret_number = random.randint(1, 10)
attempts = 3

while attempts > 0:
    guess = int(input("გამოიცანი რიცხვი (1-დან 10-მდე): "))
    if guess == secret_number:
        print("You win")
        break
    else:
        attempts -= 1
        if attempts > 0:
            print("Wrong number. დარჩენილი ცდები:", attempts)
        else:
            print("You lose. ჩაფიქრებული რიცხვი იყო:", secret_number)