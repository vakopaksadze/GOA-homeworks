# 1) მომხმარებელს შემოატანინეთ სახელი და დაბეჭდეთ ის uppercase-ში
name = input("შეიყვანეთ თქვენი სახელი: ")
print("Uppercase:", name.upper())

# 2) იგივე სახელი დაბეჭდეთ lowercase-ში
print("Lowercase:", name.lower())

# 3) იგივე სახელი დაბეჭდეთ ისე, რომ მისი პირველი ასო იყოს uppercase-ში, ხოლო დანარჩენი lowercase-ში
print("Capitalized:", name.capitalize())