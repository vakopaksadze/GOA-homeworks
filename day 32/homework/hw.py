# Slicing ნიშნავს სიის ან სტრიქონის ნაწილობრივ ამოჭრას ანუ „დაჭრას“ ინდექსების მეშვეობით.
# სინტაქსი: list[start:stop:step]
# - start: რომელი ინდექსიდან დავიწყოთ
# - stop: სად გავჩერდეთ (მაგრამ stop ინდექსი არ შედის შედეგში)
# - step: რამდენი ნაბიჯით ვიაროთ (არ არის სავალდებულო)

my_list = [10, 20, 30, 40, 50]
print(my_list[1:4])  # გამოიტანს ელემენტებს ინდექსით 1-დან 3-მდე: [20, 30, 40]





































my_list = [11, 22, 33, 44, 55, 66, 77, 88, 99, 100]

# ინდექსები იწყება 0-დან, ამიტომ:
# მე-2 ელემენტი -> ინდექსი 1
# მე-5 ელემენტი -> ინდექსი 4
# მე-8 ელემენტი -> ინდექსი 7

print(my_list[1])  # 22
print(my_list[4])  # 55
print(my_list[7])  # 88







































# მე-3 ელემენტი -> ინდექსი 2

print(my_list[2:])  # [33, 44, 55, 66, 77, 88, 99, 100]
