# სია
colors = ["red", "green", "blue", "yellow", "black"]

# მე-4 ელემენტი ნიშნავს ინდექს 3 (indexing starts from 0)
print(colors[3])  # Output: yellow




































# შეცვლა ინდექსზე 3 (yellow -> Vako)
colors[3] = "Vako"

# დაბეჭდეთ მთლიანი სია
print(colors)  # ['red', 'green', 'blue', 'Vako', 'black']





































# რიცხვების სია
