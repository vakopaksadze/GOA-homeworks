// 2) рЃерЃћрЃЦрЃЏрЃћрЃюрЃўрЃЌ рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ рЃарЃЮрЃЏрЃћрЃџрЃАрЃљрЃф рЃњрЃљрЃЊрЃљрЃћрЃфрЃћрЃЏрЃљ 2 рЃарЃўрЃфрЃ«рЃЋрЃў (start, end)
function getNumbers(start, end) {
    let numbers = [];
    for (let i = start; i <= end; i++) {
        numbers.push(i);
    }
    return numbers; // рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃЏрЃљрЃАрЃўрЃЋрЃА
}

let numArray = getNumbers(3, 10);
console.log("рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃАрЃўрЃљ:", numArray);


// 3) рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃњрЃљрЃЏрЃЮрЃЌрЃЋрЃџрЃўрЃА рЃАрЃљрЃерЃБрЃљрЃџрЃЮрЃА
function calculateAverage(arr) {
    let sum = 0;
    for (let i = 0; i < arr.length; i++) {
        sum += arr[i];
    }
    return sum / arr.length;
}

let avg = calculateAverage(numArray);
console.log("рЃАрЃљрЃерЃБрЃљрЃџрЃЮ:", avg);


// 4) even_sum рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ Рђћ рЃџрЃБрЃгрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃ»рЃљрЃЏрЃў 0-рЃўрЃЊрЃљрЃю border-рЃЏрЃЊрЃћ
function even_sum(border) {
    let sum = 0;
    for (let i = 0; i <= border; i++) {
        if (i % 2 === 0) {
            sum += i;
        }
    }
    return sum;
}

console.log("рЃџрЃБрЃгрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃ»рЃљрЃЏрЃў:", even_sum(10));


