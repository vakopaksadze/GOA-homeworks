// ­ЪЊџ рЃўрЃАрЃгрЃљрЃЋрЃџрЃћрЃЌ рЃљрЃ«рЃљрЃџрЃў рЃАрЃўрЃбрЃДрЃЋрЃћрЃЉрЃў:
// interval - рЃЊрЃарЃЮрЃўрЃА рЃЏрЃЮрЃюрЃљрЃЎрЃЋрЃћрЃЌрЃў
// timeout - рЃЊрЃарЃЮрЃўрЃА рЃљрЃЏрЃЮрЃгрЃБрЃарЃЋрЃљ


// РюЁ 2) рЃЌрЃљрЃарЃўрЃдрЃўрЃА рЃЮрЃЉрЃўрЃћрЃЦрЃбрЃў
let date = new Date(2024, 11, 25);
// рЃЌрЃЋрЃћрЃћрЃЉрЃў рЃўрЃгрЃДрЃћрЃЉрЃљ 0-рЃЊрЃљрЃю, рЃљрЃЏрЃўрЃбрЃЮрЃЏ рЃЊрЃћрЃЎрЃћрЃЏрЃЉрЃћрЃарЃў рЃљрЃарЃўрЃА 11.
// рЃћрЃА рЃцрЃЮрЃарЃЏрЃљ (year, month, day) рЃБрЃцрЃарЃЮ рЃќрЃБрЃАрЃбрЃўрЃљ, рЃЋрЃўрЃЊрЃарЃћ string-рЃўрЃА рЃњрЃљрЃЊрЃљрЃфрЃћрЃЏрЃљ ("2024-12-25").

console.log("Year:", date.getFullYear()); // 2024
console.log("Month:", date.getMonth());   // 11 (рЃЊрЃћрЃЎрЃћрЃЏрЃЉрЃћрЃарЃў)
console.log("Day:", date.getDate());      // 25
console.log("Weekday:", date.getDay());   // 3 -> рЃЮрЃЌрЃ«рЃерЃљрЃЉрЃљрЃЌрЃў (0=рЃЎрЃЋрЃўрЃарЃљ, 6=рЃерЃљрЃЉрЃљрЃЌрЃў)


// РюЁ 3) setInterval - рЃљрЃЕрЃЋрЃћрЃюрЃЮрЃА рЃљрЃЏрЃЪрЃљрЃЏрЃўрЃюрЃЊрЃћрЃџрЃў рЃЊрЃарЃЮ рЃДрЃЮрЃЋрЃћрЃџ рЃгрЃљрЃЏрЃерЃў
// setInterval рЃўрЃЏрЃћрЃЮрЃарЃћрЃЉрЃА рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃљрЃА рЃДрЃЮрЃЋрЃћрЃџ рЃЏрЃўрЃЌрЃўрЃЌрЃћрЃЉрЃБрЃџ рЃЊрЃарЃЮрЃерЃў (рЃЏрЃўрЃџрЃўрЃгрЃљрЃЏрЃћрЃЉрЃерЃў)
setInterval(() => {
  console.log("Current time:", new Date());
}, 1000); // 1000ms = 1 рЃгрЃљрЃЏрЃў


// РюЁ 4) Countdown рЃбрЃљрЃўрЃЏрЃћрЃарЃў
let time = 10;

let countdown = setInterval(() => {
  console.log("Time:", time);
  time--; // рЃДрЃЮрЃЋрЃћрЃџ рЃгрЃљрЃЏрЃерЃў рЃўрЃЎрЃџрЃћрЃЉрЃА рЃћрЃарЃЌрЃўрЃЌ

  if (time < 0) {
    clearInterval(countdown); // рЃљрЃЕрЃћрЃарЃћрЃЉрЃА рЃўрЃюрЃбрЃћрЃарЃЋрЃљрЃџрЃА
    console.log("Time up"); // рЃарЃЮрЃфрЃљ time рЃњрЃљрЃ«рЃЊрЃћрЃЉрЃљ 0 рЃљрЃю рЃюрЃљрЃЎрЃџрЃћрЃЉрЃў
  }
}, 1000);


// РюЁ 5) setTimeout рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў
// setTimeout рЃњрЃљрЃЏрЃЮрЃўрЃФрЃљрЃ«рЃћрЃЉрЃА рЃцрЃБрЃюрЃЦрЃфрЃўрЃљрЃА рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃћрЃарЃЌрЃ«рЃћрЃџ рЃЏрЃўрЃЌрЃўрЃЌрЃћрЃЉрЃБрЃџрЃў рЃЊрЃарЃЮрЃўрЃА рЃерЃћрЃЏрЃЊрЃћрЃњ
setTimeout(() => {
  console.log("1 second passed");
}, 1000);

// ­ЪЊї рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃљ:
// setTimeout Рђћ рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ рЃћрЃарЃЌрЃ«рЃћрЃџ рЃЏрЃўрЃЌрЃўрЃЌрЃћрЃЉрЃБрЃџрЃў рЃЊрЃарЃЮрЃўрЃА рЃерЃћрЃЏрЃЊрЃћрЃњ
// setInterval Рђћ рЃњрЃљрЃюрЃЏрЃћрЃЮрЃарЃЊрЃћрЃЉрЃљ рЃДрЃЮрЃЋрЃћрЃџ рЃЏрЃўрЃЌрЃўрЃЌрЃћрЃЉрЃБрЃџ рЃЊрЃарЃЮрЃерЃў


// РюЁ 6) рЃўрЃюрЃбрЃћрЃарЃЋрЃљрЃџрЃў рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃДрЃЮрЃЋрЃћрЃџ рЃгрЃБрЃЌрЃерЃў рЃќрЃарЃЊрЃўрЃА time-рЃА рЃћрЃарЃЌрЃўрЃЌ
let minutes = 0;

let minuteInterval = setInterval(() => {
  minutes++;
  console.log("Current minutes:", minutes);

  if (minutes === 34) {
    clearInterval(minuteInterval); // рЃљрЃЕрЃћрЃарЃћрЃЉрЃА рЃўрЃюрЃбрЃћрЃарЃЋрЃљрЃџрЃА
    console.log("Time up");
  }
}, 60000); // 60000 рЃЏрЃўрЃџрЃўрЃгрЃљрЃЏрЃў = 1 рЃгрЃБрЃЌрЃў

// ­ЪЊї clearInterval рЃљрЃЕрЃћрЃарЃћрЃЉрЃА setInterval-рЃА
// рЃърЃўрЃарЃЮрЃЉрЃљрЃА if(minutes === 34) рЃЋрЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃЌ, рЃарЃЮрЃЏ рЃбрЃљрЃўрЃЏрЃћрЃарЃў рЃњрЃљрЃЕрЃћрЃарЃЊрЃћрЃА рЃарЃЮрЃфрЃљ рЃЏрЃўрЃљрЃдрЃгрЃћрЃЋрЃА 34-рЃА